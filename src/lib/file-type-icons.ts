function declarationFileKind(fileName: string): FileTypeIconKind | null {
  const lower = fileName.toLowerCase();
  if (
    lower.endsWith(".d.ts") ||
    lower.endsWith(".d.mts") ||
    lower.endsWith(".d.cts")
  ) {
    return "typescript";
  }
  return null;
}

function resolveKind(language: string | undefined, fileName: string): FileTypeIconKind {
  const base = fileName.includes("/")
    ? fileName.slice(fileName.lastIndexOf("/") + 1)
    : fileName;
  const lowerBase = base.toLowerCase();

  const exact = EXACT_NAME[lowerBase];
  if (exact) return exact;

  if (looksLikeToolchainConfig(base)) return "toolchain";

  if (isEnvFile(base)) return "env";

  const decl = declarationFileKind(base);
  if (decl) return decl;

  const norm = language?.toLowerCase().trim();
  if (norm) {
    const fromAlias = LANG_ALIASES[norm];
    if (fromAlias) return fromAlias;
  }

  const dot = base.lastIndexOf(".");
  if (dot >= 0) {
    const ext = base.slice(dot).toLowerCase();
    const fromExt = EXT_TO_KIND[ext];
    if (fromExt) return fromExt;
  }

  return "default";
}

/** Uses `language` when set, then basename rules and file extension. */
export function getFileIconForNode(
  language: string | undefined,
  fileName: string
): FileTypeIconEntry {
  return fileTypeIcons[resolveKind(language, fileName)];
}

