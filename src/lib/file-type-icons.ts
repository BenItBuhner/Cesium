function looksLikeToolchainConfig(fileName: string): boolean {
  const lower = fileName.toLowerCase();
  if (/^tsconfig(\..*)?\.json$/.test(lower)) return true;
  if (/^jsconfig\.json$/.test(lower)) return true;
  if (lower === "components.json") return true;
  if (/^eslint\.config\./.test(lower)) return true;
  if (/^\.eslintrc/.test(lower)) return true;
  if (/^\.prettierrc/.test(lower)) return true;
  if (/^prettier\.config\./.test(lower)) return true;
  if (/^(postcss|tailwind|vite|vitest|rollup|webpack)\.config\./.test(lower))
    return true;
  if (/^(next|nuxt|svelte|astro)\.config\./.test(lower)) return true;
  if (/^babel\.config\./.test(lower)) return true;
  if (/^playwright\.config\./.test(lower)) return true;
  if (/^jest\.config\./.test(lower)) return true;
  if (/^vitest\.config\./.test(lower)) return true;
  if (/^\.(npmrc|yarnrc|yarnrc\.yml|pnpmrc|nvmrc|node-version|babelrc|browserslistrc)$/i.test(lower))
    return true;
  if (lower === ".editorconfig") return true;
  if (lower === ".cursorrules" || lower.endsWith(".cursorrules")) return true;
  if (lower === "next-env.d.ts") return false;
  return false;
}

function isEnvFile(fileName: string): boolean {
  const lower = fileName.toLowerCase();
  if (lower === ".env" || lower.startsWith(".env.")) return true;
  if (lower.startsWith(".envrc")) return true;
  return false;
}

