/**
 * Emits generated/web-theme.css and generated/native-theme.css from the
 * canonical token maps in src/theme-tokens.ts (via the built dist output).
 * Run after `tsc`: the package `build` script chains this automatically.
 *
 * web-theme.css (Next.js + Electron renderer):
 *   1. `:root`    — light defaults (every ThemeTokens key, resolved values)
 *   2. `html.dark` — dark defaults
 *   3. `@theme inline` — Tailwind v4 utility mapping
 *
 * native-theme.css (React Native via NativeWind/react-native-css): same
 * values, but dark tokens live under `@media (prefers-color-scheme: dark)`
 * because there is no `html.dark` element on native, and the geist font
 * mappings are omitted (native font stacks come from nativewind/theme).
 */
import { mkdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

import {
  DEFAULT_THEME_TOKENS_DARK,
  DEFAULT_THEME_TOKENS_LIGHT,
} from "../dist/theme-tokens.js";
import { DESIGN_2_SURFACE_ALIASES } from "../dist/design-language.js";

const packageRoot = join(dirname(fileURLToPath(import.meta.url)), "..");

/** Tailwind v4 theme mapping (kept in lockstep with the token set). */
const THEME_INLINE_MAPPINGS = [
  ["--color-background", "--background"],
  ["--color-foreground", "--foreground"],
  ["--color-bg-main", "--bg-main"],
  ["--color-bg-panel", "--bg-panel"],
  ["--color-bg-card", "--bg-card"],
  ["--color-bg-card-hover", "--bg-card-hover"],
  ["--color-bg-tab-active", "--bg-tab-active"],
  ["--color-bg-tab-inactive", "--bg-tab-inactive"],
  ["--color-border-card", "--border-card"],
  ["--color-border-subtle", "--border-subtle"],
  ["--color-text-primary", "--text-primary"],
  ["--color-text-secondary", "--text-secondary"],
  ["--color-text-disabled", "--text-disabled"],
  ["--color-accent", "--accent"],
  ["--color-accent-dark", "--accent-dark"],
  ["--color-accent-bg", "--accent-bg"],
  ["--color-plan-accent", "--plan-accent"],
  ["--color-plan-accent-dark", "--plan-accent-dark"],
  ["--color-plan-accent-bg", "--plan-accent-bg"],
  ["--color-debug-accent", "--debug-accent"],
  ["--color-debug-accent-dark", "--debug-accent-dark"],
  ["--color-debug-accent-bg", "--debug-accent-bg"],
  ["--color-ask-accent", "--ask-accent"],
  ["--color-ask-accent-dark", "--ask-accent-dark"],
  ["--color-ask-accent-bg", "--ask-accent-bg"],
  ["--color-burn-accent", "--burn-accent"],
  ["--color-burn-accent-dark", "--burn-accent-dark"],
  ["--color-burn-accent-bg", "--burn-accent-bg"],
  ["--color-workflow-accent", "--workflow-accent"],
  ["--color-workflow-accent-dark", "--workflow-accent-dark"],
  ["--color-workflow-accent-bg", "--workflow-accent-bg"],
  ["--color-orchestration-accent", "--orchestration-accent"],
  ["--color-orchestration-accent-dark", "--orchestration-accent-dark"],
  ["--color-orchestration-accent-bg", "--orchestration-accent-bg"],
  ["--radius-card", "--radius-card"],
  ["--radius-tab", "--radius-tab"],
  ["--radius-pill", "--radius-pill"],
  ["--font-sans", "--font-geist-sans"],
  ["--font-mono", "--font-geist-mono"],
];

function declarations(tokens) {
  return Object.entries(tokens)
    .map(([key, value]) => `  ${key}: ${value};`)
    .join("\n");
}

const header = `/**
 * AUTO-GENERATED by packages/design/scripts/generate-theme-css.mjs — DO NOT EDIT.
 * Canonical source: packages/design/src/theme-tokens.ts.
 */`;

const webThemeMappings = THEME_INLINE_MAPPINGS;
const nativeThemeMappings = THEME_INLINE_MAPPINGS.filter(
  ([, src]) => !src.startsWith("--font-geist")
);

const webCss = `${header}

:root {
${declarations(DEFAULT_THEME_TOKENS_LIGHT)}
${declarations(DESIGN_2_SURFACE_ALIASES.common)}
${declarations(DESIGN_2_SURFACE_ALIASES.light)}
}

html.dark {
${declarations(DEFAULT_THEME_TOKENS_DARK)}
${declarations(DESIGN_2_SURFACE_ALIASES.dark)}
}

@theme inline {
${webThemeMappings.map(([out, src]) => `  ${out}: var(${src});`).join("\n")}
}
`;

const nativeCss = `${header}

:root {
${declarations(DEFAULT_THEME_TOKENS_LIGHT)}
${declarations(DESIGN_2_SURFACE_ALIASES.common)}
${declarations(DESIGN_2_SURFACE_ALIASES.light)}
}

@media (prefers-color-scheme: dark) {
  :root {
${declarations(DEFAULT_THEME_TOKENS_DARK).replace(/^/gm, "  ")}
${declarations(DESIGN_2_SURFACE_ALIASES.dark).replace(/^/gm, "  ")}
  }
}

@theme inline {
${nativeThemeMappings.map(([out, src]) => `  ${out}: var(${src});`).join("\n")}
}
`;

await mkdir(join(packageRoot, "generated"), { recursive: true });
await writeFile(join(packageRoot, "generated", "web-theme.css"), webCss, "utf8");
await writeFile(join(packageRoot, "generated", "native-theme.css"), nativeCss, "utf8");
console.log("generated/web-theme.css + generated/native-theme.css written");
